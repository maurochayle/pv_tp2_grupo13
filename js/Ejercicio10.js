let num1 = parseInt (prompt("Ingresa el primer numero entero: "));
let num2 = parseInt (prompt("Ingresa el segundo numero entero: "));
let num3 = parseInt (prompt("Ingresa el tercer numero entero: "));

let promedio = parseFloat ((num1 + num2 + num3) / 3).toFixed(2);

alert("El promedio es: " + promedio);
